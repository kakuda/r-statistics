# 4.2
# 推測統計は「推定」「検定」の2つに分類される
# 推定は「点推定」「区間推定」の2つに分類される
# 推定: 具体的な値を用いて「母数の値は〇〇くらいだろう」という結論を導くもの
# 点推定: 1つの値で推定の結果を表す(例: 60点)
# 区間推定: ある程度の幅を持った区間で結果を表す(例: 50〜70点)
# 検定: 母集団について述べた異なる立場の2つの主張のうちどちらかを採択するかを決めるもの

