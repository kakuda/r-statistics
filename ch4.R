# 4.3.2
# 標本統計量: 標本データから計算されるもの(平均、分散、標準偏差、相関係数...)
# 母数の推定量: 母数を推定するために用いられる標本統計量
# 推定値: 標本データを用いて計算された推定量の値

# 推定量: データを使った計算式(関数)やその式につけられた名前(平均など)
# 推定値: 計算の結果得られる数値
# 心理統計学の基礎―統合的理解のために
# http://www.amazon.co.jp/dp/4641121605
# 母数		推定量					推定値
# 母平均		標本平均				標本データから計算された標本平均の値
# 母分散		不偏分散				標本データから計算された不偏分散の値
# 母標準偏差	不偏分散の正の平方根	標本データから計算された不偏分散の正の平方根の値
# 母相関係数	標本相関係数			標本データから計算された標本相関係数の値
# 母比率		標本比率				標本データから計算された標本比率の値
var(身長)

